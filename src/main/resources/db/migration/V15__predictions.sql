-- ===================================================================
-- V15 - Mac tahminleri (winner + percent + comparison).
--
-- API-Football /predictions?fixture={id}: cok zengin nested yapida cevap
-- (predictions.winner, predictions.percent, predictions.goals,
--  predictions.under_over, comparison.form/att/def/poisson/h2h/goals/total).
--
-- Bu tabloda en cok kullanilan alt kume saklanir (winner, advice, percent,
-- comparison) — frontend "radar chart + bahis tahmini" goruntuleri icin
-- yeterli. teams.home/away derin istatistikleri (last_5, league.fixtures.*)
-- ihtiyac duyulursa V16'da eklenir.
--
-- Tum percent ve karsilastirma degerleri VARCHAR (API "60%", "1.2" gibi
-- formatlanmis string'ler doner). Frontend parse eder.
--
-- UNIQUE(fixture_id): mac basina tek tahmin. UPSERT pattern.
-- ===================================================================

CREATE TABLE predictions (
    id              BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fixture_id      BIGINT       NOT NULL REFERENCES fixtures (id) ON DELETE CASCADE,

    -- predictions.winner
    winner_team_id  BIGINT,
    winner_comment  VARCHAR(120),
    win_or_draw     BOOLEAN,

    -- predictions
    advice          VARCHAR(255),
    under_over      VARCHAR(8),
    goals_home      VARCHAR(8),
    goals_away      VARCHAR(8),

    -- predictions.percent — home/draw/away kazanma yuzdesi (yuzdelik string)
    percent_home    VARCHAR(8),
    percent_draw    VARCHAR(8),
    percent_away    VARCHAR(8),

    -- comparison bloklari — radar chart icin (yuzdelik string)
    comparison_form_home    VARCHAR(8),
    comparison_form_away    VARCHAR(8),
    comparison_att_home     VARCHAR(8),
    comparison_att_away     VARCHAR(8),
    comparison_def_home     VARCHAR(8),
    comparison_def_away     VARCHAR(8),
    comparison_poisson_home VARCHAR(8),
    comparison_poisson_away VARCHAR(8),
    comparison_h2h_home     VARCHAR(8),
    comparison_h2h_away     VARCHAR(8),
    comparison_goals_home   VARCHAR(8),
    comparison_goals_away   VARCHAR(8),
    comparison_total_home   VARCHAR(8),
    comparison_total_away   VARCHAR(8),

    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),

    CONSTRAINT uq_predictions_fixture UNIQUE (fixture_id)
);
